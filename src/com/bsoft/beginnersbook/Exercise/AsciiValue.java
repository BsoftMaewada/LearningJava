package com.bsoft.beginnersbook.Exercise;

public class AsciiValue {

    public static void main(String[] args) {

        /*  Program to find ASCII code of a character */

        char ch = 'b';
        int asciiCode = ch;

        int asciiValues = (int) ch;

        System.out.println("ASCII value of " + ch + " is: " + asciiCode);
        System.out.println("ASCII value of " + ch + " is: " + asciiValues);

    }
}
